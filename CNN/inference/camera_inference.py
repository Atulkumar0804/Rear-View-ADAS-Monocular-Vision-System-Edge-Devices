class RearADASAlert:
    """
    Three-zone proximity alert system for rear-camera ADAS.

    Zone definitions (configurable):
        CRITICAL  < 2 m   – Immediate brake / stop
        DANGER    2 – 5 m – Slow down, audible rapid beep
        CAUTION   5 –10 m – Awareness alert, slow beep
        SAFE      > 10 m  – No alert

    Alert is triggered per-object per-frame; deduplication / throttling
    is handled by a cooldown timer so the system does not spam.

    Audio alerts use a platform-agnostic beep stub.  Replace the body of
    `_emit_audio_alert()` with your hardware beeper call.
    """

    ZONES = [
        ('CRITICAL', 0.0,  2.0,  (0,   0,   255), 3),   # (name, min, max, bgr, beep_hz_factor)
        ('DANGER',   2.0,  5.0,  (0,   80,  255), 2),
        ('CAUTION',  5.0, 10.0,  (0,  165,  255), 1),
        ('SAFE',    10.0, 999.0, (0,  200,    0), 0),
    ]

    def __init__(self, cooldown_s: float = 0.5):
        self.cooldown_s = cooldown_s
        self._last_alert_time: dict = {}   # track_id → last alert timestamp
        self._last_zone: dict = {}         # track_id → last zone name
        self.alert_log: deque = deque(maxlen=100)
        self._critical_active = False

    def classify_zone(self, distance: float) -> tuple:
        """Return (zone_name, color_bgr) for a given distance in metres."""
        for name, dmin, dmax, color, _ in self.ZONES:
            if dmin <= distance < dmax:
                return name, color
        return 'SAFE', (0, 200, 0)

    def process(self, detections: list) -> list:
        """
        Process a list of detections, add 'alert_zone' and 'alert_color' keys.
        Trigger audio alerts for new critical/danger events.
        Returns the annotated detection list.
        """
        self._critical_active = False
        now = time.time()

        for det in detections:
            if det.get('rear_filtered', False):
                det['alert_zone'] = 'SUPPRESSED'
                det['alert_color'] = (80, 80, 80)
                continue

            dist = det.get('distance')
            if dist is None:
                det['alert_zone'] = 'UNKNOWN'
                det['alert_color'] = (128, 128, 128)
                continue

            zone_name, color = self.classify_zone(float(dist))
            det['alert_zone'] = zone_name
            det['alert_color'] = color

            if zone_name == 'CRITICAL':
                self._critical_active = True

            # Throttle alerts by track_id
            track_id = det.get('track_id', -1)
            last_t = self._last_alert_time.get(track_id, 0.0)
            last_zone = self._last_zone.get(track_id, 'SAFE')

            # Fire if zone worsened or cooldown expired
            if zone_name in ('CRITICAL', 'DANGER') and (
                zone_name != last_zone or (now - last_t) > self.cooldown_s
            ):
                self._emit_audio_alert(zone_name, dist)
                self._last_alert_time[track_id] = now
                self._last_zone[track_id] = zone_name
                self.alert_log.append({
                    'time': now,
                    'track_id': track_id,
                    'zone': zone_name,
                    'class': det.get('class', '?'),
                    'distance': dist,
                    'entry_side': det.get('entry_side', 'center')
                })

        return detections

    @property
    def critical_active(self) -> bool:
        return self._critical_active

    def _emit_audio_alert(self, zone: str, distance: float):
        """
        Stub for audio alert output.
        Replace this method body with your hardware beeper / TTS call.
        On Linux you can use:  os.system("beep -f 1000 -l 200")
        """
        # Visual-only fallback: print to terminal with colour codes
        if zone == 'CRITICAL':
            print(f"\033[1;31m⚠️  CRITICAL ALERT: Object at {distance:.1f}m – STOP!\033[0m")
        elif zone == 'DANGER':
            print(f"\033[1;33m⚠️  DANGER: Object at {distance:.1f}m – SLOW DOWN\033[0m")

    def draw_alert_overlay(self, frame: np.ndarray, config: dict) -> np.ndarray:
        """
        Draw a rear-proximity arc guide at the bottom of the frame.
        The arc shows the three zones (CRITICAL/DANGER/CAUTION) as
        colour bands – standard in commercial reverse-camera displays.
        """
        h, w = frame.shape[:2]
        overlay = frame.copy()

        # Draw three concentric arc bands at bottom-centre
        cx = w // 2
        cy = h + int(h * 0.35)   # arc centre is below the frame bottom

        zones_display = [
            ('CAUTION',  int(w * 0.90), (0, 165, 255)),
            ('DANGER',   int(w * 0.60), (0, 80, 255)),
            ('CRITICAL', int(w * 0.35), (0, 0, 255)),
        ]

        for zone_name, radius, color in zones_display:
            thickness = 4 if (zone_name == 'CRITICAL' and self._critical_active) else 2
            cv2.ellipse(overlay, (cx, cy), (radius, radius),
                        0, 200, 340, color, thickness)
            # Zone label
            label_angle = 270  # top of arc
            lx = int(cx + radius * math.cos(math.radians(label_angle - 90)))
            ly = int(cy + radius * math.sin(math.radians(label_angle - 90)))
            ly = max(0, ly)
            lx = max(10, min(w - 60, lx))
            cv2.putText(overlay, zone_name, (lx - 30, ly + 5),
                        cv2.FONT_HERSHEY_SIMPLEX, 0.35, color, 1)

        # Flash entire frame border RED when CRITICAL
        if self._critical_active:
            border = 6
            cv2.rectangle(overlay, (0, 0), (w - 1, h - 1), (0, 0, 255), border)

        alpha = 0.55
        cv2.addWeighted(overlay, alpha, frame, 1 - alpha, 0, frame)
        return frame


class DepthCalibrator:
    """
    Smart Hybrid Calibration for Monocular Depth Estimation.
    Uses geometric anchors (known vehicle heights) to dynamically calibrate
    the relative depth map from the neural network.
    Implements temporal smoothing to prevent jitter.
    """
    def __init__(self, window_size=60, default_scale=1325.0):
        self.window_size = window_size
        self.scale_history = []
        self.current_scale = default_scale
        
    def update(self, anchors, depth_map):
        """
        Update scale based on geometric anchors.
        anchors: list of dicts with 'bbox', 'class', 'confidence'
        """
        frame_scales = []
        for anchor in anchors:
            bbox = anchor['bbox']
            cls = anchor['class']
            
            # Geometric distance (Pinhole Model)
            h = bbox[3] - bbox[1]
            if h <= 0: continue
            
            # Estimate distance using pinhole
            # Distance = (Real Height * Focal Length) / Pixel Height
            real_height = REAL_HEIGHTS.get(cls, 1.5)
            geo_dist = (real_height * FOCAL_LENGTH) / h
            
            # Get relative depth from model
            # Depth Anything: Higher value = Closer
            # So Distance ~ Scale / Depth_Value  => Scale = Distance * Depth_Value
            d_crop = depth_map[bbox[1]:bbox[3], bbox[0]:bbox[2]]
            if d_crop.size == 0: continue
            
            rel_depth = np.median(d_crop)
            if rel_depth <= 0: continue
            
            # Calculate scale for this object
            scale = geo_dist * rel_depth
            frame_scales.append(scale)
            
        if frame_scales:
            # Median of this frame's anchors to reject outliers
            frame_scale = np.median(frame_scales)
            
            # Sanity check: Don't let scale jump too wildly (e.g. > 50% change)
            if self.scale_history:
                avg_scale = np.mean(self.scale_history)
                if 0.5 * avg_scale < frame_scale < 2.0 * avg_scale:
                    self.scale_history.append(frame_scale)
            else:
                self.scale_history.append(frame_scale)
            
            # Keep history size
            if len(self.scale_history) > self.window_size:
                self.scale_history.pop(0)
                
            # Update current smoothed scale (Simple Moving Average)
            self.current_scale = np.mean(self.scale_history)
            
        return self.current_scale
        
    def get_scale(self):
        return self.current_scale

# ── Speed-adaptive alert zone calculation ────────────────────────────────────
def get_speed_adaptive_zones(speed_kmh: float) -> dict:
    """
    Compute ADAS proximity zone boundaries from vehicle speed.

    Physics:
        Reaction time = 1.5 s  (human + sensor latency)
        Deceleration  = 5 m/s²  (0.5 g – achievable on wet/gravel road)
        Stopping dist = v × t_r  +  v² / (2 × a)

    Returns a dict of zone boundaries (metres) that can be merged into any
    camera config dict (e.g. TWOWHEELER_CAMERA_CONFIG or REAR_CAMERA_CONFIG).

    Example::
        cfg = dict(TWOWHEELER_CAMERA_CONFIG)
        cfg.update(get_speed_adaptive_zones(gps_speed_kmh))
    """
    v = speed_kmh / 3.6                   # m/s
    stopping = v * 1.5 + v ** 2 / (2 * 5.0)
    return {
        "zone_critical_m": max(3.0, stopping * 0.5),
        "zone_danger_m":   max(8.0, stopping),
        "zone_caution_m":  max(20.0, stopping * 2.0),
    }


# ── ECC-based vibration stabilisation (two-wheeler / motorcycle) ─────────────
def stabilize_frame(prev_gray: "np.ndarray | None",
                    curr_gray: np.ndarray,
                    curr_frame: np.ndarray) -> np.ndarray:
    """
    Compensate for motorcycle engine vibration using ECC image stabilisation.

    Estimates a Euclidean warp (rotation + translation) between the previous
    and current grayscale frames and applies the inverse to the colour frame.
    Falls back to the original frame on failure (blurred scene, large motion).

    Typical usage::
        prev_gray = None
        while cap.isOpened():
            ret, frame = cap.read()
            curr_gray = cv2.cvtColor(frame, cv2.COLOR_BGR2GRAY)
            frame = stabilize_frame(prev_gray, curr_gray, frame)
            prev_gray = curr_gray
    """
    if prev_gray is None:
        return curr_frame
    try:
        warp = np.eye(2, 3, dtype=np.float32)
        criteria = (cv2.TERM_CRITERIA_EPS | cv2.TERM_CRITERIA_COUNT, 50, 1e-4)
        _, warp = cv2.findTransformECC(
            prev_gray, curr_gray, warp, cv2.MOTION_EUCLIDEAN, criteria
        )
        h, w = curr_frame.shape[:2]
        return cv2.warpAffine(
            curr_frame, warp, (w, h),
            flags=cv2.INTER_LINEAR + cv2.WARP_INVERSE_MAP,
            borderMode=cv2.BORDER_REPLICATE,
        )
    except cv2.error:
        return curr_frame


# ── Pipeline watchdog ─────────────────────────────────────────────────────────
class PipelineWatchdog(threading.Thread):
    """
    Background watchdog that detects main-loop stalls on edge devices.

    Call ``heartbeat()`` once per successfully processed frame.  If more than
    ``timeout_s`` seconds elapse without a heartbeat, ``self.stalled`` is set
    to ``True`` so the main loop can reinitialise the capture device.

    Usage::
        wd = PipelineWatchdog(timeout_s=3.0)
        wd.start()
        while True:
            ret, frame = cap.read()
            wd.heartbeat()
            if wd.stalled and not is_file:
                cap.release()
                cap = open_camera_with_retry(camera_id)
                wd.reset()
    """

    def __init__(self, timeout_s: float = 3.0):
        super().__init__(daemon=True, name="PipelineWatchdog")
        self.timeout_s = timeout_s
        self.stalled = False
        self._last_beat = time.time()
        self._running = True

    def heartbeat(self):
        """Call once per successfully processed frame."""
        self._last_beat = time.time()
        self.stalled = False

    def reset(self):
        """Reset the stall flag after camera recovery."""
        self._last_beat = time.time()
        self.stalled = False

    def stop(self):
        self._running = False

    def run(self):
        _log = logging.getLogger("ADAS.Watchdog")
        while self._running:
            time.sleep(0.25)
            if time.time() - self._last_beat > self.timeout_s and not self.stalled:
                _log.critical(
                    "Pipeline stalled (no heartbeat for %.1f s) – "
                    "flagging for camera reinitialisation", self.timeout_s
                )
                self.stalled = True


# ── Camera reconnect with automatic retry ────────────────────────────────────
def open_camera_with_retry(camera_id, max_retries: int = 5,
                            retry_delay_s: float = 1.0):
    """
    Open a V4L2 camera device (or video file) with automatic retries.

    On Jetson / embedded Linux, V4L2 devices can be momentarily unavailable
    after a USB / CSI reconnect.  This function retries up to ``max_retries``
    times before raising a ``RuntimeError``.

    Args:
        camera_id:      Integer device ID, digit string, or video file path.
        max_retries:    Maximum open attempts (default 5).
        retry_delay_s:  Seconds to wait between attempts (default 1.0).

    Returns:
        An opened ``cv2.VideoCapture`` object.

    Raises:
        RuntimeError: If all attempts fail.
    """
    _log = logging.getLogger("ADAS.Camera")
    is_file = isinstance(camera_id, str) and not str(camera_id).isdigit()
    for attempt in range(1, max_retries + 1):
        if is_file:
            cap = cv2.VideoCapture(camera_id)
        else:
            cam_idx = int(camera_id) if isinstance(camera_id, str) else camera_id
            cap = cv2.VideoCapture(cam_idx, cv2.CAP_V4L2)
        if cap.isOpened():
            _log.info("Camera '%s' opened on attempt %d/%d",
                      camera_id, attempt, max_retries)
            return cap
        _log.warning(
            "Camera '%s' open failed (attempt %d/%d), retrying in %.1f s …",
            camera_id, attempt, max_retries, retry_delay_s,
        )
        cap.release()
        if attempt < max_retries:
            time.sleep(retry_delay_s)
    raise RuntimeError(
        f"Cannot open camera '{camera_id}' after {max_retries} attempts"
    )


# ── Depth backend loader with graceful fallback ───────────────────────────────
def load_depth_with_fallback(device: str, zoedepth_interval: int) -> "object | None":
    """
    Load the best available depth backend in order of quality/speed.

    Fallback order:
        1. DA2-KITTI metric ONNX            – best accuracy (SOTA fine-tuned)
        2. DA2-KITTI metric PT checkpoint   – explicit user-trained checkpoint
        3. MiDaS-KITTI metric ONNX          – strong fallback
        4. TRT FP16 (midas_small engine)    – fastest baseline
        5. ONNX (original midas_small)      – portable GPU/CPU
        6. PyTorch                           – slow but always available
        7. None                              – ground-plane geometry only

    Returns the initialised ``AsyncDepthLite`` instance, or ``None``.
    """
    _log = logging.getLogger("ADAS.DepthLoader")
    if not _DEPTH_LITE_AVAILABLE:
        _log.warning("jetson_depth_lite not available – depth disabled")
        return None

    da2_onnx = CNN_DIR / "models" / "depth_lite" / "da2_kitti_metric.onnx"
    da2_pt   = CNN_DIR / "models" / "depth_lite" / "da2_kitti_metric.pt"

    # Explicit user-preferred model #1: DA2-KITTI metric ONNX
    if da2_onnx.exists():
        try:
            model = AsyncDepthLite(
                backend="onnx",
                device=device,
                update_interval_frames=zoedepth_interval,
                model_path=str(da2_onnx),
                metric_output=True,
            )
            _log.info("✅ Metric depth loaded [DA2-KITTI ONNX]: %s", da2_onnx.name)
            return model
        except Exception as exc:
            _log.warning("DA2-KITTI ONNX failed (%s) – trying DA2 PT", exc)

    # Explicit user-preferred model #2: DA2-KITTI PT checkpoint
    if da2_pt.exists():
        try:
            model = AsyncDepthLite(
                backend="pytorch",
                device=device,
                update_interval_frames=zoedepth_interval,
                model_path=str(da2_pt),
                metric_output=True,
            )
            _log.info("✅ Metric depth loaded [DA2-KITTI PT]: %s", da2_pt.name)
            return model
        except Exception as exc:
            _log.warning("DA2-KITTI PT failed (%s) – trying MiDaS-KITTI ONNX", exc)

    # Remaining metric fallback: MiDaS-KITTI ONNX
    for label, onnx_path in [
        ("MiDaS-KITTI", CNN_DIR / "models" / "depth_lite" / "midas_kitti_metric.onnx"),
    ]:
        if onnx_path.exists():
            try:
                model = AsyncDepthLite(
                    backend="onnx",
                    device=device,
                    update_interval_frames=zoedepth_interval,
                    model_path=str(onnx_path),
                    metric_output=True,
                )
                _log.info("✅ Metric depth loaded [%s]: %s", label, onnx_path.name)
                return model
            except Exception as exc:
                _log.warning("%s ONNX failed (%s) – trying next", label, exc)

    for backend in ("trt_fp16", "onnx", "pytorch"):
        try:
            model = AsyncDepthLite(
                backend=backend,
                device=device,
                update_interval_frames=zoedepth_interval,
            )
            _log.info("Depth backend loaded: %s", backend)
            return model
        except Exception as exc:
            _log.warning("Depth backend '%s' failed: %s", backend, exc)
    _log.error("All depth backends failed – ground-plane estimation only")
    return None


